    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'title'=>'required|max:255|min:5'
        ]);
         $journalsubmit = new journalsubmit;
        $journalsubmit->user_id=auth()->user()->id;
        $journalsubmit->abstract=$request->input('abstract');
        $journalsubmit->maintext=$request->input('maintext');
       
         $journalsubmit->figures=$request->input('figures');
        $journalsubmit->tables=$request->input('tables');
        $journalsubmit->save();

        return redirect('/journal_submit')->with('success','Submitted');
    }

